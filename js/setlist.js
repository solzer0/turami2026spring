// 새 SETLIST UI: 티켓 카드 6개 + 모달
document.addEventListener("DOMContentLoaded", () => {
  const groupsContainer = document.getElementById("setlistGroups");
  const modal = document.getElementById("setlistModal");
  const modalTitle = document.getElementById("setlistModalTitle");
  const modalMeta = document.getElementById("setlistModalMeta");
  const modalLyrics = document.getElementById("setlistModalLyrics");
  const modalClose = document.getElementById("setlistModalClose");

  if (!groupsContainer || !modal) return;

  // 원하는 대로 제목만 바꿔서 쓰면 됩니다.
  const groups = [
    {
      label: "1-1. 경외 敬畏",
      titles: [
        "zeitgeist - 칵스 (THE KOXX)",
        "토마토살인사건 - 데카당",
        "나르키소스 - 설수민 자작곡",
        "멸종 - 쏜애플",
      ],
    },
    {
      label: "1-2. 자유로운우리를봐자유로워",
      titles: [
        "시간을 달리네 - 한로로",
        "큰새 - 혁오",
        "Find Me! - The Poles",
        "클라우드 쿠쿠 랜드 - 정우",
      ],
    },
    {
      label: "2-1. 청량락",
      titles: [
        "Rubato - 요루시카",
        "Zutto Zutto Zutto - 녹황색사회",
        "불 - 유다빈밴드",
        "축배 - 유다빈밴드",
      ],
    },
    {
      label: "2-2. 옥신크린",
      titles: [
        "널 안 봄 - 옥민준 자작곡",
        "적도 - 전기뱀장어",
        "Marigolds - Tokai",
        "RUN - 신한영 자작곡",
      ],
    },
    {
      label: "3-1. novana project",
      titles: [
        "sandman + 우주선 - 박동헌, 추예림 자작곡",
        "라스트패스트 - 박동헌 자작곡",
        "별과 혼 - 이서윤 자작곡",
      ],
    },
    {
      label: "3-2. 샤빠뚜비두바",
      titles: [
        "집에 - 새소년",
        "Marunouchi Sadistic - Tokyo Jihen (동경사변)",
        "Never Too Much - Luther Vandross",
        "멸종위기사랑 - 이찬혁",
      ],
    },
  ];

  function openModalByTitle(title) {
    const song = songs.find((s) => s.title === title);

    if (!song) {
      // " - " 이전 부분만 표시
      const displayTitle = title.includes(" - ")
        ? title.split(" - ")[0]
        : title;
      modalTitle.textContent = displayTitle;
      modalMeta.textContent = "상세 정보가 아직 준비되지 않았습니다.";
      modalLyrics.textContent = "";
    } else {
      // " - " 이전 부분만 표시
      const displayTitle = song.title.includes(" - ")
        ? song.title.split(" - ")[0]
        : song.title;
      modalTitle.textContent = displayTitle;
      modalMeta.innerHTML = song.meta.replaceAll("\n", "<br>");
      modalLyrics.textContent = song.lyrics || "";
    }

    modal.classList.add("open");
    modal.setAttribute("aria-hidden", "false");
    document.body.style.overflow = "hidden";
  }

  function closeModal() {
    modal.classList.remove("open");
    modal.setAttribute("aria-hidden", "true");
    modalTitle.textContent = "";
    modalMeta.textContent = "";
    modalLyrics.textContent = "";
    document.body.style.overflow = "";
  }

  // 카드 + 곡 버튼 생성
  groups.forEach((group) => {
    const card = document.createElement("article");
    card.className = "setlist-card";

    const header = document.createElement("div");
    header.className = "setlist-card-header";
    header.textContent = group.label;

    const body = document.createElement("div");
    body.className = "setlist-card-body";

    group.titles.forEach((title) => {
      const btn = document.createElement("button");
      btn.type = "button";
      btn.className = "setlist-song";
      btn.textContent = title; // 필요하면 '제목 - 가수' 형식으로 변경
      btn.addEventListener("click", () => openModalByTitle(title));
      body.appendChild(btn);
    });

    card.appendChild(header);
    card.appendChild(body);

    groupsContainer.appendChild(card);
  });

  // 모달 닫기 이벤트
  if (modalClose) {
    modalClose.addEventListener("click", closeModal);
  }
  modal.addEventListener("click", (e) => {
    if (e.target === modal) closeModal();
  });
  document.addEventListener("keydown", (e) => {
    if (e.key === "Escape" && modal.classList.contains("open")) {
      closeModal();
    }
  });
});
